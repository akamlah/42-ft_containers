/* ************************************************************************ */
/*																			*/
/*			Some ANSI escape sequences to use in output						*/
/*																			*/
/* ************************************************************************ */

#pragma once
#ifndef __ANSIPALETTE_H__
# define __ANSIPALETTE_H__

/* ------------------------------------------------------------------------ */

#ifndef MAGENTA
# define MAGENTA "\033[0;35m"
#endif /* MAGENTA */

#ifndef MAGENTA_B
# define MAGENTA_B "\033[1;35m"
#endif /* MAGENTA_B */

#ifndef RED
# define RED "\033[0;31m"
#endif /* RED */

#ifndef SL_YELLOW
# define SL_YELLOW "\033[38;5;227m"
#endif /* SL_YELLOW */

#ifndef YELLOW
# define YELLOW "\033[0;33m"
#endif /* SL_YELLOW */

#ifndef GREEN
# define GREEN "\033[0;32m"
#endif /* GREEN */

#ifndef GREEN_0
# define GREEN_0 "\033[0;92m"
#endif /* GREEN */

#ifndef GREEN_1
# define GREEN_1 "\033[38;5;64m"
#endif /* GREEN_1 */

#ifndef GREEN_2
# define GREEN_2 "\033[38;5;29m"
#endif /* GREEN_2 */

#ifndef GREEN_3
# define GREEN_3 "\033[38;5;22m"
#endif /* GREEN_3 */

#ifndef CYAN
# define CYAN "\033[0;36m"
#endif /* CYAN */

#ifndef CYAN_B
# define CYAN_B "\033[1;36m"
#endif /* CYAN_B */

#ifndef BLUE
# define BLUE "\033[0;34m"
#endif /* BLUE */

#ifndef L_BLUE
# define L_BLUE "\033[38;5;117m"
#endif /* L_BLUE */

#ifndef L_GREEN_BLUE
# define L_GREEN_BLUE "\033[38;5;49m"
#endif /* L_GREEN_BLUE */

#ifndef L_BLUE_GREEN
# define L_BLUE_GREEN "\033[38;5;50m"
#endif /* L_BLUE_GREEN */

#ifndef ORANGE
# define ORANGE "\033[38;5;166m"
#endif /* ORANGE */

#ifndef NC
# define NC "\033[0m"
#endif /* NC */

/* ------------------------------------------------------------------------ */
#endif /* __ANSIPALETTE_H__ */

